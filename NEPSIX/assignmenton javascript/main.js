// javascript code for the Area Of A Trapezium
// Formula = 1/2(a+b)h
// let 1/2 be x
// ! start

const x = 1/2


let a = 5
let b = 4
let h = 6
const y = a + b
let areaofTrapezium = x * y * h

console.log(`The Area of a Trapezium is ${areaofTrapezium}`)

